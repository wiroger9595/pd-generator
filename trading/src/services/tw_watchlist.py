def get_default_tw_watchlist() -> list[dict]:
    """預設台股 watchlist，去重 + 加 .TW 後綴"""
    seen = set()
    out = []
    for group in (MEMORY, SEMICONDUCTOR, AI_SERVER, SEMI_EQUIP, FINANCE):
        for code, name in group:
            if code in seen:
                continue
            seen.add(code)
            out.append({"ticker": f"{code}.TW", "name": name})
    return out
